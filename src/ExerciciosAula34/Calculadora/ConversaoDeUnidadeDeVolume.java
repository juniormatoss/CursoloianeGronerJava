package ExerciciosAula34.Calculadora;

public class ConversaoDeUnidadeDeVolume {
    

    public static double ConversaoDeUnidadesDeVolume(double numero){
            double guardar = 0;
            for (int i = 0; i < numero; i++){
                guardar = guardar + 929;
            }
            return guardar;
        
    }
}
